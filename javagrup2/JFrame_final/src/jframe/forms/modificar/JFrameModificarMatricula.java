        element.setData_baixa(" ");
        element.setEstat("Actiu");
        Alumne alumne = ll_a.buscarPerNom(nomAlumne.getText());
        Grup grup = ll_g.buscarPerNom(nomGrup.getText());

        ll_m.modificarMatricula(selectedItem, nomInstitut.getText(), alumne, grup);

        // Tornem a la finestra principal
