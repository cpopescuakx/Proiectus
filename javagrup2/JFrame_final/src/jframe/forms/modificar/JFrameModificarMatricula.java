    private void botoModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoModificarActionPerformed
        //Mostrem missatge de confirmació
        int confirm = JOptionPane.showConfirmDialog(this, "Esteu segurs de modificar aquesta matricula?");
        if (confirm == JOptionPane.YES_OPTION) {
            //Cridem al metode per a modificar les dades
            Alumne alumne = ll_a.buscarPerNom(nomAlumne.getText());
            Grup grup = ll_g.buscarPerNom(nomGrup.getText());

            ll_m.modificarMatricula(selectedItem, nomInstitut.getText(), alumne, grup);

            // Tornem a la finestra principal
            JFrameMatricula principalWindow = new JFrameMatricula(ll_m, ll_a, ll_g, ll_p);
            principalWindow.setVisible(true);       //la fem visible
            dispose();                             //eliminem la finestra actual
        }
    }//GEN-LAST:event_botoModificarActionPerformed

