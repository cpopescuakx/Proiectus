    private void borrarGrupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_borrarGrupMouseClicked
        if (selectedItem != -1) { // selectedItem s'inicialitza amb -1, si no es -1 entra al if
            // Mostrem un missatge de confirmació i fem que conf sigui el que showConfirmDialog retorna
            int conf = JOptionPane.showConfirmDialog(this, "Esteu segurs de borrar aquest grup?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (conf == JOptionPane.YES_OPTION) { // Si hem clickat a Confirmar, entra al if, borra l'element seleccionat, refresca la taula i fica selectedItem a -1 (ningun seleccionat)
                ll_g.borrar(selectedItem);
                emplenar_taula("");
                selectedItem = -1;
            }
        } else { // Si no s'ha seleccionat cap element, mostra un missatge d'error
            JOptionPane.showMessageDialog(this, "Per a realitzar aquesta acció heu de seleccionar un grup de la taula", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_borrarGrupMouseClicked

