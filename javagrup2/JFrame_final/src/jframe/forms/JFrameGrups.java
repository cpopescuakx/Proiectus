    private void modificarGrupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarGrupMouseClicked
        if (selectedItem != -1) {
            int conf2 = JOptionPane.showConfirmDialog(this, "Esteu segurs de modificar les dades d'aquest grup?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if(conf2 == JOptionPane.YES_OPTION){
               JFrameModificarGrups g_a = new JFrameModificarGrups(ll_m, ll_a, ll_g, ll_p, selectedItem);
                selectedItem = -1;
                g_a.setVisible(true);
                dispose(); 
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Per a realitzar aquesta acció heu de seleccionar un grup de la taula", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_modificarGrupMouseClicked

