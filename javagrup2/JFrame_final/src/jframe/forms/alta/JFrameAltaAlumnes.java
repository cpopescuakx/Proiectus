    /* Agafa el text de cada camp per a crear un alumne i afegir-lo a la llista */

    private void botoAfegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoAfegirActionPerformed

        String valorNom = fieldNom.getText();
        String valorCognom = fieldCognom.getText();
        String valorEmail = fieldEmail.getText();
        String valorDni = fieldDNI.getText();
        String valorData = fieldData.getText();
        String valorCentre = fieldCentre.getText();

        if (valorNom.equals("") || valorCognom.equals("") || valorEmail.equals("") || valorDni.equals("") || valorData.equals("") || valorCentre.equals("")) {
            JOptionPane.showMessageDialog(null, "No poden quedar camps buits");
        } else {
            ll_a.afegirAlumne(valorNom, valorCognom, valorEmail, valorDni, valorData, valorCentre);
            this.setVisible(false);
            JFrameAlumnes fll = new JFrameAlumnes(ll_m, ll_a, ll_g, ll_p);
            fll.setVisible(true);
            fll.setLocationRelativeTo(null);
            fll.setTitle("GestiÃ³ d'alumnes");
            dispose();
        }
    }//GEN-LAST:event_botoAfegirActionPerformed

